package samples.demo;

public class Location {
   private String name;
   private String matricno;
   
   void setName(String name) {
	   this.name=name;
   }
   
   String getName() {
	   return name;
   }
   
   void setMatricno(String matricno) {
	   this.matricno=matricno;
   }
   
   String getMatricno() {
	   return matricno;
   }
}
